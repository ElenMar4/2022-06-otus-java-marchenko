package ru.otus;

public class Calculator {

    public int calculateAdding (int numberOne, int numberTwo){
        return numberOne + numberTwo;
    }

    public int calculateSubtraction (int numberOne, int numberTwo){
        return numberOne - numberTwo;
    }

    public int calculateMultiplication (int numberOne, int numberTwo){
        return numberOne * numberTwo;
    }

}
